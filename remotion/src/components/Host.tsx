// ─────────────────────────────────────────────────────────────────────────────
// Host — the fixed HonestStack brand mascot.
//
// Two modes:
//   1. IDLE-TALK (default): the two-frame Pharaoh (mouth-closed / mouth-open),
//      audio-driven mouth swap, glides slowly across the lower band. This is
//      what every segment defaults to when no pharaoh_pose is set.
//
//   2. POSE: when the segment carries a pharaoh_pose, the Pharaoh swaps to
//      the matching pose PNG from the pose library (generated by
//      generate-pharaoh-poses via Gemini I2I, same character identity across
//      all poses). Each pose has its own anchor + entry animation so the
//      Pharaoh "peeks", "points", "celebrates" etc. into the scene with intent.
//
// All Pharaoh PNGs are on solid white backgrounds (Gemini doesn't reliably
// emit true alpha) — the WhiteKeyImage component knocks the white out to
// transparency once per asset and memoises the result.
// ─────────────────────────────────────────────────────────────────────────────

// Bundled mascot frames — fixed, identical in every render (idle-talk mode).
const PHARAOH_CLOSED = staticFile("pharaoh-closed.png");
const PHARAOH_OPEN = staticFile("pharaoh-open.png");

// Pose library — staticFile-loaded from public/poses/. Each PNG was
// generated by generate-pharaoh-poses with the original mascot as reference,
// so character identity is preserved across the set. Keep these IDs in sync
// with the editorial brain's pharaoh_pose enum.
export type PharaohPose =
  | "idle-talk"
  | "peek-left"
  | "peek-right"
  | "point-up-right"
  | "point-down"
  | "surprised"
  | "sarcastic-shrug"
  | "count-fingers"
  | "walk-in-left"
  | "walk-out-right"
  | "crying"
  | "celebrating";

// Per-pose render config.
//   src       — path to pose PNG (null = use idle-talk two-frame system)
//   anchor    — where the Pharaoh sits in the lower band
//   animation — how it enters the frame
//   scaleMul  — multiplier on the base CHAR_HEIGHT (some poses need to be
//               bigger — peeks/celebrating — others fit standard size)
//   facing    — overrides facing (1 = right, -1 = left). null = no override.
interface PosePreset {
  src: string | null;
  anchor: "glide" | "bottom-left" | "bottom-center" | "bottom-right" |
    "edge-left" | "edge-right";
  animation: "glide" | "slide-from-left" | "slide-from-right" |
    "walk-from-left" | "walk-out-right" | "fade-in" | "scale-pop";
  scaleMul: number;
  facing: 1 | -1 | null;
}

